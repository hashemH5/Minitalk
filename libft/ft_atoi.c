#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	sign;
	int	i;
	int	out;

	i = 0;
	out = 0;
	sign = 1;
	while ((nptr[i] >= '\t' && nptr[i] <= '\r') || nptr[i] == ' ')
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign = -sign;
		i++;
	}
	while (ft_isdigit(nptr[i]))
	{
		out = out * 10 + (nptr[i] - '0');
		i++;
	}
	return (out * sign);
}
