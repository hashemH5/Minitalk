#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	long	x;

	x = n ;
	if (x < 0)
	{
		write(fd, "-", 1);
		x = -x;
	}
	if (x / 10)
		ft_putnbr_fd(x / 10, fd);
	c = (x % 10) + '0';
	write(fd, &c, 1);
}
